package edu.java.variable06;

public class LiteralMain {

	public static void main(String[] args) {
		
		System.out.println("리터럴(Literal)");
		
		int integer1 = 123456789; 
		long integer2 = 12345678900l; // long 타입은 뒤에 l or L 추가
		double decimal1 = 3.1415;
		float decimal2 = 3.14f; // float 타입은 뒤에 f or F 추가
		
		char ch1 = 'A'; //문자는 기본 char
		String str1 = "문자열"; // 문자열 기본 String
		
		System.out.println(integer2);
		System.out.println(decimal2);
		
	}
}
