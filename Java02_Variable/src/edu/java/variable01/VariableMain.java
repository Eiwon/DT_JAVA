package edu.java.variable01;

public class VariableMain {
	
	//프로그램 시작점 : main()
	public static void main(String[] args) {
		// println() : 데이터를 console 창에 출력
		System.out.println("변수(variable)");
		// 자동완성 단축키 : ctrl + space
		
		int x = 10;
		int y;
		
		System.out.println("x = " + x);
		y = 20;
		System.out.println("y = " + y);
		
		x = 12 + y;
		y = 10;
		System.out.println("x = " + x);
		System.out.println("y = " + y);
		
	}// end main()
	
	// end VariableMain
}
