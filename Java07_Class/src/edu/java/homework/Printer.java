package edu.java.homework;

public class Printer {
	public void println(int val) {
		System.out.println("int 타입 : " + val);
	}
	public void println(boolean val) {
		System.out.println("boolean 타입 : " + val);
	}
	public void println(double val) {
		System.out.println("double 타입 : " + val);
	}
	public void println(String val) {
		System.out.println("String 타입 : " + val);
	}
}
